      <p class="text-justify">
      Luchar por ser el mejor es una de las premisas fundamentales de la vida. Desde el nacimiento todo ser busca la supervivencia, es una constante competición frente al entorno. Quien se adapta es quien permanece. Los seres humanos no somos una excepción, de hecho, competir está tan dentro de nosotros que hemos creado los juegos con ese objetivo. Tras la búsqueda de la victoria no se encuentra ni la fama ni el dinero, sino la superación.<br>
      <br>
      No es de extrañar que, ya en los años 80, con los primeros videojuegos, se organizaran grandes torneos a los que asistieron cientos de participantes. ¿Quién tendría los mejores reflejos? ¿Quién desarrollaría la mejor estrategia? ¿Quién sería el más rápido mentalmente?<br>
      <br>
      Es probable que si eres un amante de los esports ya conozcas su magnitud. Las competiciones profesionales de videojuegos no solo atraen a los jugadores, son seguidas a lo largo de todo el mundo, llenando estadios desde Seúl hasta Berlín, pasando por Nueva York o Madrid. Hoy en día son unos de los mayores eventos mundiales en audiencia.<br>
      <br>
      Actualmente, cerca de 300 millones de personas siguen de forma habitual los deportes electrónicos. La final de League of Legends alcanzó los 36, más que la final de la NBA o del mundial de Rugby. </p>
